                        rect = elm.getBoundingClientRect(),
                        scrollDelta;

                    // check if element is in viewport
                    if ((rect.top - window.innerHeight) <= 0 && rect.bottom > 0)
                        startPos = startPos === undefined ? fromTop : startPos;
